def can_move(grid, x, y):
    """
    Check if the player can move to the given grid coordinates
    x and y are grid coordinates, not pixel coordinates
    """
    grid_x = int(x // 16)
    grid_y = int(y // 16)

    # Check if the new position is within grid bounds
    if grid_x < 0 or grid_x >= len(grid[0]) or grid_y < 0 or grid_y >= len(grid):
        return False

    # Check if the new position is a wall (grid value of 1)
    return grid[grid_y][grid_x] != 1


def main():
    player = pygame.Rect(24 * 16, 19 * 16, 16, 16)
    monsters = [
        pygame.Rect(16, 19, 16, 16),  # First monster
        pygame.Rect(63 * 16, 19, 16, 16),  # Second monster starts from right
        pygame.Rect(61 * 16, 19 , 16, 16)  # Third monster starts from top
    ]
    monster_colors = ["white", "red", "green"]
    monster_image = pygame.transform.scale(pygame.image.load("images/monster1.png"), (16, 16))

    monster_image2 = pygame.transform.scale(pygame.image.load("images/monster3.png"), (16, 16))
    monster_image3 = pygame.transform.scale(pygame.image.load("images/monster6.png"), (16, 16))
    # Different colors for each monster

    run = True
    clock = pygame.time.Clock()
    start_time = time.time()
    elapsed_time = 0
    monster_move_delay = 0
    MONSTER_MOVE_INTERVAL = 15

    while run:
        clock.tick(60)
        elapsed_time = time.time() - start_time

        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                run = False
                break

        WIN.fill((121, 168, 157))

        # Draw walls
        for i in range(grid_height):
            for j in range(grid_width):
                if grid[i][j] == 1:
                    BG = pygame.transform.scale(pygame.image.load("images/pixilart-drawing.png"), (16, 16))
                    WIN.blit(BG, (j * 16, i * 16))

        # Handle player movement
        keys = pygame.key.get_pressed()
        if keys[pygame.K_LEFT] and can_move(grid, player.x - PLAYER_VEL, player.y):
            player.x -= PLAYER_VEL
        if keys[pygame.K_RIGHT] and can_move(grid, player.x + PLAYER_VEL, player.y):
            player.x += PLAYER_VEL
        if keys[pygame.K_UP] and can_move(grid, player.x, player.y - PLAYER_VEL):
            player.y -= PLAYER_VEL
        if keys[pygame.K_DOWN] and can_move(grid, player.x, player.y + PLAYER_VEL):
            player.y += PLAYER_VEL

        # Update monsters position less frequently
        monster_move_delay += 1
        if monster_move_delay >= MONSTER_MOVE_INTERVAL:
            for monster in monsters:
                start_x = int(monster.x / 16)
                start_y = int(monster.y / 16)
                end_x = int(player.x / 16)
                end_y = int(player.y / 16)

                start_x = max(0, min(grid_width - 1, start_x))
                start_y = max(0, min(grid_height - 1, start_y))
                end_x = max(0, min(grid_width - 1, end_x))
                end_y = max(0, min(grid_height - 1, end_y))

                path = astar.haha(grid, (start_y, start_x), (end_y, end_x))
                if path and len(path) > 1:
                    next_pos = path[1]
                    monster.x = next_pos[1] * 16
                    monster.y = next_pos[0] * 16
            monster_move_delay = 0

        # Check collisions with all monsters
        for monster in monsters:
            if player.colliderect(monster):
                run = False
                print("Game Over!")
                break

        # Draw everything
        pygame.draw.rect(WIN, "blue", player)
        for monster, color in zip(monsters, monster_colors):
            pygame.draw.rect(WIN, color, monster)
        WIN.blit(monster_image, monsters[0])
        WIN.blit(monster_image2, monsters[1])
        WIN.blit(monster_image3, monsters[2])
        draw(0, elapsed_time, player, monsters[0])  # Using first monster for compatibility with draw function

        pygame.display.update()

    pygame.quit()


if __name__ == "__main__":
    main()


